// Function Declaration
function sayHi(name) {
  console.log("Bonjour, ", name);
}

// sayHi("David Beckham");

// Function Expression

// sayHi2("David Beckham");

const sayHi2 = function (name) {
  console.log("Bonjour, ", name);
};

// Arrow Functions

const sayHi3 = (name) => {
  console.log("Hello", name)
}

// Function Invocation

sayHi3("Rick Sanchez")
